function randomizeImages() {
	for (let i = 0; i < images.length; i++) {
		let random = Math.floor(Math.random() * images.length); // gives random index for swap

		// swap indices
		let temp = images[random];
		images[random] = images[i];
		images[i] = temp;
	}
}

function imageNext() {
	console.log("next image");
		console.log(imageCtr);
	console.log(singleImage.innerHTML);

	if (imageCtr == images.length - 1) {
		imageCtr = 0;
	} else {
  		imageCtr++;
	}

	singleImage.innerHTML= images[imageCtr];
}

function captionNext() {
	console.log("next");
		console.log(captionCtr);
	console.log(text.innerHTML);

	if (captionCtr == captions.length - 1) {
		captionCtr = 0;
	} else {
  		captionCtr++;
	}

	text.innerHTML= captions[captionCtr];
}

function captionBack() {
	console.log("back");
			console.log(captionCtr);

	console.log(text.innerHTML);

	if (captionCtr == 0) {
		captionCtr = captions.length - 1;
	} else {
  		captionCtr--;
	}

		text.innerHTML= captions[captionCtr];

} 

function switchGalleryView() {
	console.log("gallery");

	if (!galleryToggle) {
		captionCol.remove();
		galleryToggle = true;
	} else if (galleryToggle) {
		document.body.appendChild(captionCol);
		galleryToggle = false;
	}

	galleryButton.classList.toggle("gallery-button-toggled");
	imageCol.classList.toggle("image-col-gallery");
	imageSingle.classList.toggle("image-col-single-gallery");
	// get all image cell images and add the hovering image cell gallery class
}

