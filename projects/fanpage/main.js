document.addEventListener("keyup", (e) => {
	if (e.code === "ArrowLeft" || e.code === "ArrowUp") {
		captionBack();
	} else if (e.code === "ArrowRight" || e.code === "ArrowDown") {
		captionNext();
	}
}); 

